export function getByCategory(cat: Product["category"]) {
  return products.filter((p) => p.category === cat);
}

export function getBySlug(slug: string) {
  return products.find((p) => p.slug === slug);
}

export function getBySubcategory(sub: string) {
  return products.filter((p) => p.subcategory.toLowerCase() === sub.toLowerCase());
}
